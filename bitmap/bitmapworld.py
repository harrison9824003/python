import sys
# 預設世界地圖
bitmap ="""
....................................................................
   **************   *  *** **  *      ******************************
  ********************* ** ** *  * ****************************** *
 **      *****************       ******************************
          *************          **  * **** ** ************** *
           *********            *******   **************** * *
            ********           ***************************  *
   *        * **** ***         *************** ******  ** *
               ****  *         ***************   *** ***  *
                 ******         *************    **   **  *
                 ********        *************    *  ** ***
                   ********         ********          * *** ****
                   *********         ******  *        **** ** * **
                   *********         ****** * *           *** *   *
                     ******          ***** **             *****   *
                     *****            **** *            ********
                    *****             ****              *********
                    ****              **                 *******   *
                    ***                                       *    *
                    **     *                    *
....................................................................
"""

print('請輸入文字, 用來構建世界地圖')
string = input('> ')
if string == '':
    sys.exit()

# 逐一讀取 bitmap 每行文字
for line in bitmap.splitlines():
    # 逐一讀取每行中每個文字
    for i, bit in enumerate(line):
        # 若為空格或是點狀, 輸出原本內容不做替換
        if bit == ' ':
            print(' ', end='')
        elif bit == '.':
            print('.', end='')
        else:
            print(string[i % len(string)], end='')
    print()
